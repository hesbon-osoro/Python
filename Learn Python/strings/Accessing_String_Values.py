#Accessing Values in Strings
var1 = 'Hello World'
var2 = 'Python Programming'
print ("var1[0]: ", var1[0]) 
print ("var2[1:5]: ", var2[1:5])
print("var1[-len(var1):]:",var1[-len(var1):])
print("var1[-len(var1)]:",var1[-len(var1)])
print("var1[:-len(var1)]:",var1[:-len(var1)])
print("var1[-5:2]:",var1[-5:2])
print("var1[2:-5]:",var1[2:-5])
print("",var1*3)

