#Get the unique elements of a list
names = ['Grace','Sharon','Lynne','Alice','Iris','Lesley','Linah','Messy','Grace','Alice']
nums = [1,2,3,4,5,2,1,3,2,1,3]
print("Lists")
print("names:",names)
print("nums:",nums)
print("len(names):",len(names))
print("len(nums):",len(nums))
print()
print("Unique List Elements")
print("set(names):",set(names))
print("set(nums):",set(nums))
print("len(set(names)):",len(set(names)))
print("len(set(nums)):",len(set(nums)))
print()
print("Unique List Items")
print("list(set(names)):",list(set(names)))
print("list(set(nums)):",list(set(nums)))
