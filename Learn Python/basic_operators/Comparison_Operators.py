#Comparison Operators
a = 10
b = 20

#equal ==
print(b,' == ',a,' : ',b==a)
#not equal !=
print(b,' != ',a,' : ',b!=a)
#greater than >
print(b,' > ',a,' : ',b>a)
#less than <
print(b,' < ',a,' : ',b<a)
#greater than or equal to
print(b,' >= ',a,' : ',b>=a)
#less than or equal to
print(b,' <= ',a,' : ',b<=a)
