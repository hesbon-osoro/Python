#Arithmetic Operators

#Variables
a = 10
b = 20
#Addition
print(a,' + ',b,' = ',a+b)

#Subtraction
print(b,' - ',a,' = ',b-a)

#Multiplication
print(a,' x ',b,' = ',a*b)

#Division
print(b,' / ',a,' = ',b/a)

#Modulus
print(b,' % ',a,' = ',b%a)

#Exponent
print(b,' ** ',a,' = ',b**a)

#Floor Division
print(b,' // ',a,' = ',b//a)